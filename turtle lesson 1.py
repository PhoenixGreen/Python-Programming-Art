from turtle import *

# Pen settings
pensize(5)
shape("turtle")

# Letter H
left(90)
forward(100)
back(50)
right(90)
forward(40)
left(90)
forward(50)
back(100)

# move to start of new letter
penup()
right(90)
forward(40)
left(90)
pendown()

# Letter i
forward (50)
penup()
forward(25)

# Words complete
done()
